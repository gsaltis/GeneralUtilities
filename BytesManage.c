/*****************************************************************************!
 * Function : ByteManageSwap8
 *****************************************************************************/
uint64_t
ByteManageSwap8
(uint64_t InData)
{
  int					j, i;
  uint8_t*				data8;
  uint8_t				data[8];
  uint64_t*				data64;
 
  data8 = (uint8_t*)&InData;
  for (j = 7, i = 0; i < 8; i++, j--) {
    data[j] = data8[i];
  }
  data64 = (uint64_t*)data;
  return *data64;
}

